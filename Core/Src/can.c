void CAN_send_data(int8_t data1, int8_t data2)
{
  CAN_TxHeaderTypeDef TxHeader;
  uint32_t TxMailbox;
  uint8_t data[8] = {0}; // Example data
  data[0]=data1;
  data[2]=data2;
  // Set up the CAN message header
  TxHeader.StdId = 0x321; // Standard identifier
  TxHeader.ExtId = 0x01; // Extended identifier
  TxHeader.RTR = CAN_RTR_DATA; // Data frame
  TxHeader.IDE = CAN_ID_STD; // Standard ID
  TxHeader.DLC = 8; // Data length code

  // Transmit the CAN message
  if (HAL_CAN_AddTxMessage(&hcan2, &TxHeader, data, &TxMailbox) != HAL_OK) {
  // Transmission request Error
  Error_Handler();
  }
}
